import pytest

from src.processing import filter_by_state, sort_by_date


@pytest.fixture
def sample_data():
    """Фикстура для тестирования функций filter_by_state и sort_by_date."""
    return [
        {"id": 1, "state": "EXECUTED", "date": "2023-12-01"},
        {"id": 2, "state": "PENDING", "date": "2023-12-03"},
        {"id": 3, "state": "EXECUTED", "date": "2023-11-30"},
        {"id": 4, "state": "EXECUTED", "date": "2023-12-02"},
        {"id": 5, "state": "CANCELED", "date": "2023-12-04"},
    ]


def test_filter_by_state_1():
    data = [
        {"id": 41428829, "state": "EXECUTED", "date": "2019-07-03T18:35:29.512364"},
        {"id": 939719570, "state": "EXECUTED", "date": "2018-06-30T02:08:58.425572"},
        {"id": 594226727, "state": "CANCELED", "date": "2018-09-12T21:27:25.241689"},
        {"id": 615064591, "state": "CANCELED", "date": "2018-10-14T08:21:33.419441"},
    ]
    assert filter_by_state(data, "EXECUTED") == [
        {"id": 41428829, "state": "EXECUTED", "date": "2019-07-03T18:35:29.512364"},
        {"id": 939719570, "state": "EXECUTED", "date": "2018-06-30T02:08:58.425572"},
    ]


